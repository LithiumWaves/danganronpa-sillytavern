#!/usr/bin/env node
/*
 * Export gift/item sprites from a local SillyTavern settings file into the
 * extension so they ship with a download.
 *
 * Item sprites are normally stored per-user as base64 data URIs in
 * extension_settings["danganronpa-extension"].inventory.itemImages. That keeps
 * them out of the repo, so a fresh install shows blank image slots. This script
 * decodes those data URIs to real files under assets/images/items/ and
 * regenerates items/bundledItemSprites.js, which the panel uses as a fallback
 * when the user hasn't uploaded their own image for an item.
 *
 * Usage:
 *   node tools/export-item-sprites.mjs [path/to/settings.json]
 *
 * Default settings path assumes the standard SillyTavern layout
 * (.../SillyTavern/data/default-user/settings.json).
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const extRoot = path.resolve(__dirname, "..");
const EXTENSION_KEY = "danganronpa-extension";

// .../SillyTavern/public/scripts/extensions/third-party/danganronpa-extension
// -> .../SillyTavern/data/default-user/settings.json
const defaultSettingsPath = path.resolve(
    extRoot,
    "../../../../../data/default-user/settings.json",
);
const settingsPath = process.argv[2] || defaultSettingsPath;

const outDir = path.join(extRoot, "assets", "images", "items");
const manifestPath = path.join(extRoot, "items", "bundledItemSprites.js");

const MIME_EXT = {
    "image/webp": "webp",
    "image/png": "png",
    "image/jpeg": "jpg",
    "image/jpg": "jpg",
    "image/gif": "gif",
    "image/svg+xml": "svg",
};

function readItemImages(file) {
    const raw = JSON.parse(fs.readFileSync(file, "utf8"));
    const ext = raw.extension_settings?.[EXTENSION_KEY];
    const images = ext?.inventory?.itemImages;
    if (!images || typeof images !== "object") {
        throw new Error(
            `No inventory.itemImages found under extension_settings["${EXTENSION_KEY}"] in ${file}`,
        );
    }
    return images;
}

function parseDataUri(value) {
    const m = /^data:([^;,]+)(;base64)?,(.*)$/s.exec(value || "");
    if (!m) return null;
    const mime = m[1].toLowerCase();
    const isBase64 = !!m[2];
    const ext = MIME_EXT[mime];
    if (!ext) return null;
    const buf = isBase64
        ? Buffer.from(m[3], "base64")
        : Buffer.from(decodeURIComponent(m[3]), "utf8");
    return { ext, buf };
}

function main() {
    if (!fs.existsSync(settingsPath)) {
        console.error(`Settings file not found: ${settingsPath}`);
        console.error("Pass the path explicitly: node tools/export-item-sprites.mjs <settings.json>");
        process.exit(1);
    }

    const images = readItemImages(settingsPath);
    fs.mkdirSync(outDir, { recursive: true });

    // Drop any previously exported sprites so removed/renamed ids don't linger.
    for (const f of fs.readdirSync(outDir)) {
        fs.rmSync(path.join(outDir, f));
    }

    const manifest = {};
    let written = 0;
    let skipped = 0;
    for (const id of Object.keys(images).sort()) {
        const parsed = parseDataUri(images[id]);
        if (!parsed) {
            skipped++;
            continue;
        }
        const fileName = `${id}.${parsed.ext}`;
        fs.writeFileSync(path.join(outDir, fileName), parsed.buf);
        manifest[id] = fileName;
        written++;
    }

    const lines = Object.keys(manifest)
        .sort()
        .map((id) => `    ${JSON.stringify(id)}: ${JSON.stringify(manifest[id])},`)
        .join("\n");

    const moduleSource = `// AUTO-GENERATED by tools/export-item-sprites.mjs — do not edit by hand.
// Maps item ids to their bundled sprite file under assets/images/items/.
// Used as a fallback when the user hasn't uploaded their own image for an item.
export const BUNDLED_ITEM_SPRITES = {
${lines}
};
`;
    fs.writeFileSync(manifestPath, moduleSource);

    console.log(`Wrote ${written} sprites to ${path.relative(extRoot, outDir)}/`);
    if (skipped) console.log(`Skipped ${skipped} entries (not a recognized data URI).`);
    console.log(`Regenerated ${path.relative(extRoot, manifestPath)}`);
}

main();
